/**
* Loops allow us to repeat codes
* Types: for, while, for...in, for...of, do...while
*/

//Print a number from 1 to 10
/** for(
 *       [Initial expression]; 
 *       [condition];
 *       [incrementalExpression]
 * )
 */

for (let i = 1; i <= 10; i++) {
    console.log(i); //Anything you put in here will be dispLAYED 10 TIMES
};